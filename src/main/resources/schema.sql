DROP TABLE IF EXISTS TEXT;

CREATE TABLE IF NOT EXISTS TEXT (
     UID UUID PRIMARY KEY,
     ID INT,
     TS TIMESTAMP,
     CHANNEL VARCHAR(255),
     FROM_USER VARCHAR(255),
     FOR_USER VARCHAR(255),
     TEXT VARCHAR(255)
);

DROP TABLE IF EXISTS AUDIO;

CREATE TABLE IF NOT EXISTS AUDIO (
    UID UUID PRIMARY KEY,
    ID INT,
    TS TIMESTAMP,
    CHANNEL VARCHAR(255),
    FROM_USER VARCHAR(255),
    FOR_USER VARCHAR(255),
    TYPE VARCHAR(255),
    CODEC VARCHAR(255),
    CODEC_HEADER VARCHAR(255),
    PACKET_DURATION INT
);
DROP TABLE IF EXISTS LOCATION;

CREATE TABLE IF NOT EXISTS LOCATION (
    UID UUID PRIMARY KEY,
    ID INT,
    TS TIMESTAMP,
    CHANNEL VARCHAR(255),
    FROM_USER VARCHAR(255),
    FOR_USER VARCHAR(255),
    FORMATTED_ADDRESS VARCHAR(255),
    LONGITUDE DOUBLE,
    LATITUDE DOUBLE,
    ACCURACY DOUBLE
);

DROP TABLE IF EXISTS IMAGE;

CREATE TABLE IF NOT EXISTS IMAGE (
    UID UUID PRIMARY KEY,
    ID INT,
    TS TIMESTAMP,
    CHANNEL VARCHAR(255),
    FROM_USER VARCHAR(255),
    FOR_USER VARCHAR(255),
    TYPE VARCHAR(255),
    SOURCE VARCHAR(255),
    HEIGHT INT,
    WIDTH INT
);
