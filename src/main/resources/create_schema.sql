CREATE TABLE IF NOT EXISTS TEXT (
     UUID UUID PRIMARY KEY,
     ID INT,
     TIMESTAMP TIMESTAMP,
     CHANNEL VARCHAR(255),
     FROMUSER VARCHAR(255),
     FORUSER VARCHAR(255),
     TEXT VARCHAR(255)
);

CREATE TABLE IF NOT EXISTS AUDIO (
    UUID UUID PRIMARY KEY,
    ID INT,
    TS TIMESTAMP,
    CHANNEL VARCHAR(255),
    FROM_USER VARCHAR(255),
    FOR_USER VARCHAR(255),
    TYPE VARCHAR(255),
    CODEC VARCHAR(255),
    CODEC_HEADER VARCHAR(255),
    PACKET_DURATION INT
);

CREATE TABLE IF NOT EXISTS LOCATION (
    UUID UUID PRIMARY KEY,
    ID INT,
    TS TIMESTAMP,
    CHANNEL VARCHAR(255),
    FROM_USER VARCHAR(255),
    FOR_USER VARCHAR(255),
    FORMATTED_ADDRESS VARCHAR(255),
    LONGITUDE DOUBLE,
    LATITUDE DOUBLE,
    ACCURACY DOUBLE
);

CREATE TABLE IF NOT EXISTS IMAGE (
    UUID UUID PRIMARY KEY,
    ID INT,
    TS TIMESTAMP,
    CHANNEL VARCHAR(255),
    FROM_USER VARCHAR(255),
    FOR_USER VARCHAR(255),
    TYPE VARCHAR(255),
    SOURCE VARCHAR(255),
    HEIGHT INT,
    WIDTH INT
);
